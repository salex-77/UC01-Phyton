n1=int(input("digite o primeiro numero para realizar as quatro operaçoes"))
n2=int(input("digite o segundo numero para realizar as quatro operaçoes"))
soma=n1+n2
subtração=n1-n2
multiplicação=n1*n2
divisão=n1/n2

print(f"o resultado de {n1}+{n2} é: {soma}")
print(f"o resultado de {n1}-{n2} é: {subtração}")
print(f"o resultado de {n1}x{n2} é: {multiplicação}")
print(f"o resultado de {n1}/{n2} é: {divisão}")